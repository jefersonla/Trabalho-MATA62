    public BoardLabelsVertical(String[] labels) {
        initComponents();
        
        /* Tamanho horizontal Ã© o tamanho total dividido pela quantidade de elementos */
        int verticalLength   = 432 / labels.length;
        int horizontalLength = 20;
        
        /* Aux JLabel */
        JLabel auxLabel;
        
        for(String label : labels){
            /* Create an aux label*/
            auxLabel = new JLabel();
            
            /* Configurate the label */
            auxLabel.setFont(new java.awt.Font("Dialog", 1, 18));
            auxLabel.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
            auxLabel.setText(label);
            auxLabel.setPreferredSize(new java.awt.Dimension(horizontalLength, verticalLength));
            
            /* Add label */
            add(auxLabel);
        }
    }

